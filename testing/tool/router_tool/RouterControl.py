    def __init__(self, router_type: str, *, display: bool = True, address: str | None = None) -> None:
        yaml_path = os.path.join(os.getcwd(), "config", "config_basic.yaml")
        yaml_info = yamlTool(yaml_path).yaml_read()
        self.router_type = router_type
        self.router_info = yaml_info["router"]["router_type"][router_type]
        self.yaml_info = yaml_info
        self.xpath = yamlTool(
            os.path.join(os.getcwd(), "config", "router_xpath", f"{router_type}_xpath.yaml")
        ).yaml_read()
        self.address = address or yaml_info["router"]["address"]
        self.ping_address = yaml_info["router"]["ping_address"]
        self.display = display

    def close_browser(self) -> None:
        return None

    def browser_api_not_implemented(self, action: str) -> None:
        raise NotImplementedError(
            f"{action} is reserved for the future Playwright router implementation."
        )

    def login(self) -> None:
        self.browser_api_not_implemented("Router browser login")
